APP_TIMER_DEF(m_app_timer_id);

static void app_timer_timeout_handler(void *p_context)
{
    uint16_t voltage = 0;
    battery_voltage_get(&voltage);
    NRF_LOG_INFO("Voltage is %d mV", voltage);
}


/**@brief Function for the Timer initialization.
 *
 * @details Initializes the timer module. This creates and starts application timers.
 */
static void timers_init(void)
{
    // Initialize timer module.
    ret_code_t err_code = app_timer_init();
    APP_ERROR_CHECK(err_code);

    //Repeated timer to keep track of time
    err_code = app_timer_create(&m_app_timer_id, APP_TIMER_MODE_REPEATED, app_timer_timeout_handler);
    APP_ERROR_CHECK(err_code);

}


/**@brief Function for starting timers.
 */
static void application_timers_start(void)
{
    uint32_t err_code;
    err_code = app_timer_start(m_app_timer_id, APP_TIMER_TICKS(2000), NULL);
    APP_ERROR_CHECK(err_code);
}


/**@brief Function for initializing the nrf log module.
 */
static void log_init(void)
{
    ret_code_t err_code = NRF_LOG_INIT(NULL);
    APP_ERROR_CHECK(err_code);

    NRF_LOG_DEFAULT_BACKENDS_INIT();
}


/**@brief Function for initializing power management.
 */
static void power_management_init(void)
{
    ret_code_t err_code;
    err_code = nrf_pwr_mgmt_init();
    APP_ERROR_CHECK(err_code);
}


/**@brief Function for handling the idle state (main loop).
 *
 * @details If there is no pending log operation, then sleep until next the next event occurs.
 */
static void idle_state_handle(void)
{
    if (NRF_LOG_PROCESS() == false)
    {
        nrf_pwr_mgmt_run();
    }
}

////******************************************


/**@brief Function for application main entry.
 */
int main(void)
{
    log_init();
    timers_init();
    battery_voltage_init();
    power_management_init();

    application_timers_start();
    NRF_LOG_INFO("Entering Idle State");
    for (;;)
    {
        idle_state_handle();;
    }
}